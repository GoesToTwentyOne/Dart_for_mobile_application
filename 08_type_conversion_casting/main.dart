main(){
  //string to int
  var intconv =int.parse('1');
  if (intconv==1){
    print(intconv);
  }
  // string to double
  var doubleconv =double.parse('1.55');
  if (doubleconv==1.55){
    print(doubleconv);
  }

  //int to string
  var intconv1=1.toString();
  print(intconv1);


  //double to string
  var intconv2=1.55.toString();
  print(intconv2);


}
